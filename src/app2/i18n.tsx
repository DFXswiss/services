export type Language = 'en' | 'de' | 'it' | 'fr';

/** Canonical key set (the `en` dict has the fullest coverage — every language
 * falls back to `en` for any key it's missing, then to the raw key, exactly
 * like the static app's `t()`). */
export type TranslationKey = keyof typeof en;

const dictionaries: Record<Language, Dict> = { en, de, it, fr };

/** Ordered for the language switcher, same order/labels as the static app.
 * `flag` resolves to src/app2/assets/flags/<flag>.svg; `de` uses the plain
 * German flag (the static app's `de_or` id always resolved to it too). */
export const LANGUAGES: { code: Language; label: string; flag: string }[] = [
  { code: 'en', label: 'English', flag: 'gb' },
  { code: 'de', label: 'Deutsch', flag: 'de' },
  { code: 'it', label: 'Italiano', flag: 'it' },
  { code: 'fr', label: 'Français', flag: 'fr' },
];

const STORAGE_KEY = 'dfx_lang';

function isLanguage(value: string): value is Language {
  return value in dictionaries;
}

function detectLanguage(): Language {
  if (typeof window === 'undefined') return 'en';

  try {
    const fromQuery = new URLSearchParams(window.location.search).get('lang');
    if (fromQuery && isLanguage(fromQuery)) return fromQuery;
  } catch {
    // malformed query string — ignore and fall through
  }

  try {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (stored && isLanguage(stored)) return stored;
  } catch {
    // localStorage unavailable (private mode, sandboxed embed, ...)
  }

  const nav = (window.navigator.language || 'en').slice(0, 2).toLowerCase();
  return isLanguage(nav) ? nav : 'en';
}

function translate(language: Language, key: TranslationKey, vars?: Record<string, string | number>): string {
  let value = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
  if (vars) {
    for (const name of Object.keys(vars)) {
      value = value.split(`{${name}}`).join(String(vars[name]));
    }
  }
  return value;
}

interface LanguageContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextValue | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(detectLanguage);

  const setLanguage = useCallback((next: Language) => {
    setLanguageState(next);
    try {
      window.localStorage.setItem(STORAGE_KEY, next);
    } catch {
      // persistence is a nicety, not a requirement
    }
  }, []);

  const t = useCallback(
    (key: TranslationKey, vars?: Record<string, string | number>) => translate(language, key, vars),
    [language],
  );

  const value = useMemo<LanguageContextValue>(() => ({ language, setLanguage, t }), [language, setLanguage, t]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

/**
 * `const { t, language, setLanguage } = useT();` from any app2 screen/component.
 * `t(key, vars?)` mirrors the static app's `t()` — falls back en -> raw key,
 * and interpolates `{varName}` tokens against `vars`.
 */
export function useT() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useT must be used within a LanguageProvider');
  return ctx;
}
